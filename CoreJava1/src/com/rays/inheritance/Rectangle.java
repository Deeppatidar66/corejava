package com.rays.inheritance;

public class Rectangle extends Shape {
	private int lenght = 0;
	private int with = 0;
	
	public void setLenght(int lenght) {
		this.lenght = lenght;
	}
	public int getLenght() {
		return lenght;
	}
	public void setWith(int with) {
		this.with = with;
	}
	public int getWith() {
		return with;
	}

}
